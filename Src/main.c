int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART3_UART_Init();
  MX_LWIP_Init();
  MX_IWDG_Init();
  MX_USART6_UART_Init();

  HAL_UART_Receive_DMA(&huart6, RtkData, RTK_BUFFER_SIZE);
  RingBuffer_DMA rbd;
  RingBuffer_DMA_Init(&rbd, &hdma_usart6_rx, RtkData, RTK_BUFFER_SIZE);

  Console_WriteLn("");
  Console_WriteLn("Start.");
  HAL_UART_Transmit(&huart6, (uint8_t*)"test\r\n", 6, 1000);
  if (netif_is_link_up(&gnetif))
  {
	  u32_t adr = gnetif.ip_addr.addr;
	  while(adr == 0)
	  {
		  MX_LWIP_Process();
		  adr = gnetif.ip_addr.addr;
	  }

	  SetIsNetUp(1);
	  char msg[100];
	  sprintf(msg, "Net is up: %d.%d.%d.%d", (int)((adr>>0)%0x100), (int)((adr>>8)%0x100),
			  (int)((adr>>16)%0x100), (int)((adr>>24)%0x100));
	  Console_WriteLn(msg);
  }
  else
  {
	  SetIsNetUp(0);
	  Console_WriteLn("Net is down");
  }

  Tcp_NtripCaster_Init();

  while (1)
  {

	  MX_LWIP_Process();

	  DoWatchDogRefresh();
	  DoGreenBlinking();
	  DoSendingCorrection(&rbd);
  }

}

